// ResolveRolePrompt возвращает промпт предустановленной роли по её имени, либо
// сам введённый текст как кастомную роль. Пустой ввод даёт пустую строку.
func ResolveRolePrompt(input string) string {
	trimmed := strings.TrimSpace(input)
	for _, r := range PredefinedRoles {
		if strings.EqualFold(trimmed, r.Name) {
			return r.Prompt
		}
	}
	return trimmed
}

// BuildSystemPrompt оборачивает роль в инструкции, чтобы модель полностью
// вживалась в образ и не отвечала как нейтральный ассистент.
func BuildSystemPrompt(role string) string {
	return "Ты полностью вживаешься в роль и общаешься только в образе.\n\n" +
		"Твоя роль:\n" + role + "\n\n" +
		"Правила:\n" +
		"- Всегда оставайся в образе, отвечай от первого лица, в характере и манере этой роли.\n" +
		"- Не выходи из роли и не упоминай, что ты ИИ, бот или языковая модель; не описывай роль со стороны — будь ей.\n" +
		"- Сохраняй тон, лексику и стиль, свойственные роли; реагируй эмоционально, как живой персонаж.\n" +
		"- Отвечай естественно и по сути, как ответил бы этот персонаж в реальном разговоре."
}
